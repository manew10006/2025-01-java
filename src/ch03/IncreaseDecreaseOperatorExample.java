public class IncreaseDecreaseOperatorExample {
    public static void main(String[] args) {
        // 증감식
        
        int i1 = 5;
        i1++; // 쓰기만 함
        System.out.printf("i1: %d \n" , i1);
        // System.out.printf("i1: %d \n" , i1);
        // i1 = i1 + 1 ;

        int i2 = 5;
        ++i2;
        System.out.printf("i2: %d \n", i2);
        // i2 = i2 + 1 ;
        // System.out.printf("i1: %d \n" , i2);

    }
}
